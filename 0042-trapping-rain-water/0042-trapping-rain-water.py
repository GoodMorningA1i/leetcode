class Solution:
    def trap(self, height: List[int]) -> int:
        res = 0 
        l = 0
        r = len(height) - 1
        leftMax, rightMax = height[l], height[r]

        while l < r:
            if leftMax <= rightMax:
                l += 1
                leftMax = max(leftMax, height[l])
                res += max(leftMax - height[l], 0)
            else:
                r -= 1
                rightMax = max(rightMax, height[r])
                res += max(rightMax - height[r], 0)

        return res

